    private void cargarInforme() {
        try {
            // Establecer una fuente monoespaciada para asegurar la alineaci√≥n de columnas
            Informe_plantas.setFont(new Font("Monospaced", Font.PLAIN, 12));
            
            // Instanciar el servicio/controlador
            ControladorInformePlantas servicio = new ControladorInformePlantas();
            
            // Generar el informe y establecerlo en el JTextArea
            String informeCompleto = servicio.generarInformeFormateado();
            
            Informe_plantas.setText(informeCompleto);
            
            // Opcional: mover el scroll al inicio
            Informe_plantas.setCaretPosition(0); 

        } catch (Exception e) {
            Informe_plantas.setText("ERROR: No se pudo generar el informe.\n" + e.getMessage());
            System.err.println("Error al cargar el informe en la vista: " + e.getMessage());
        }
    }
    
