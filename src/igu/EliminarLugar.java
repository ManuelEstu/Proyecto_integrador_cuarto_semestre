    private void Btn_eliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Btn_eliminarActionPerformed
        // 1. Validar que todos los campos obligatorios est√©n llenos
        if (!validarCamposVacios()) {
            return; // Detiene el proceso si hay campos vac√≠os.
        }

        String numero_ica = Txt_num_ICA.getText().trim();

        // *************************************************************************
        // *** 3. LLAMADA AL CONTROLADOR Y MANEJO DE LA RESPUESTA ***
        // *************************************************************************

        // Llama al m√©todo del controlador, el cual maneja todas las validaciones
        // de formato (d√≠gitos, longitud, etc.) y el guardado en la BD.
        String resultadoEliminacion = control.eliminarLugar(
            numero_ica
        );

        if (resultadoEliminacion.startsWith("‚úÖ")) {
            // √âxito: El registro fue eliminado.
           JOptionPane.showMessageDialog(this,
                            resultadoEliminacion, // Muestra el mensaje con el ICA
                            "Eliminaci√≥n Exitosa", JOptionPane.INFORMATION_MESSAGE);

            // Opcional: Limpiar los campos despu√©s de una eliminaci√≥n exitosa
            limpiarCampos();
        
        } else if (resultadoEliminacion.startsWith("üö´")) { // <-- NUEVA CONDICI√ìN CLAVE
            // Error de Restricci√≥n de Clave For√°nea (FK)
            JOptionPane.showMessageDialog(this,
                    resultadoEliminacion, // El mensaje ya est√° bien detallado en el Controlador
                    "Error de Integridad de Datos", JOptionPane.ERROR_MESSAGE);

        }else if (resultadoEliminacion.startsWith("‚ùå")) {
            JOptionPane.showMessageDialog(this,
                            "Error en la eliminaci√≥n: " + resultadoEliminacion,
                            "Error de Validaci√≥n/Registro", JOptionPane.ERROR_MESSAGE);

        } else if (resultadoEliminacion.startsWith("‚ö†Ô∏è")) {
            JOptionPane.showMessageDialog(this,
                            "Error en la eliminaci√≥n, no existe: " + resultadoEliminacion,
                            "Error", JOptionPane.ERROR_MESSAGE);

        } 
    }//GEN-LAST:event_Btn_eliminarActionPerformed
    
    private void limpiarCampos() {
        Txt_num_ICA.setText(""); 
    }
    
    private boolean validarCamposVacios() {
        // Campos de texto comunes
        if (Txt_num_ICA.getText().trim().isEmpty()){
            
            JOptionPane.showMessageDialog(this, "Debe llenar todos los campos obligatorios.", "Error de Validaci√≥n", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        
        return true;
    }
    
