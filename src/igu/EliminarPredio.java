    private ControladorEliminarPredio control = new ControladorEliminarPredio();
    private void Btn_eliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Btn_eliminarActionPerformed
        // 1. Validar que todos los campos obligatorios estén llenos
        if (!validarCamposVacios()) {
            return; // Detiene el proceso si hay campos vacíos.
        }

        String numero_ica = Txt_num_ICA.getText().trim();

        // *************************************************************************
        // *** 3. LLAMADA AL CONTROLADOR Y MANEJO DE LA RESPUESTA ***
        // *************************************************************************

        // Llama al método del controlador, el cual maneja todas las validaciones
        // de formato (dígitos, longitud, etc.) y el guardado en la BD.
        String resultadoEliminacion = control.eliminarPredio(
            numero_ica
        );

        if (resultadoEliminacion.startsWith("✅")) {
            // Éxito: El registro fue eliminado.
            JOptionPane.showMessageDialog(this,
                resultadoEliminacion, // Muestra el mensaje con el ICA
                "Eliminación Exitosa", JOptionPane.INFORMATION_MESSAGE);

            // Opcional: Limpiar los campos después de una eliminación exitosa
            limpiarCampos();

        }else if (resultadoEliminacion.startsWith("❌")) {
            JOptionPane.showMessageDialog(this,
                "Error en la eliminación: " + resultadoEliminacion,
                "Error de Validación/Registro", JOptionPane.ERROR_MESSAGE);

        } else if (resultadoEliminacion.startsWith("⚠️")) {
            JOptionPane.showMessageDialog(this,
                "Error en la eliminación, no existe: " + resultadoEliminacion,
                "Error", JOptionPane.ERROR_MESSAGE);

        }

    }//GEN-LAST:event_Btn_eliminarActionPerformed
    
    private void limpiarCampos() {
        Txt_num_ICA.setText(""); 
    }
    
    private boolean validarCamposVacios() {
        // Campos de texto comunes
        if (Txt_num_ICA.getText().trim().isEmpty()){
            
            JOptionPane.showMessageDialog(this, "Debe llenar todos los campos obligatorios.", "Error de Validación", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        
        return true;
    }
    
