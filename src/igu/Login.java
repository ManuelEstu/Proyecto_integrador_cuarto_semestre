    private void Btn_InicioSesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Btn_InicioSesActionPerformed
        // 1. Obtener los datos de la interfaz
        String usuario = Txt_ing_usuario.getText().trim();
        String clave = new String(Txt_ing_clave.getPassword()).trim();
        // Obtener el tipo de usuario seleccionado del JComboBox
        String tipoUsuario = (String) SeleccionarUsuario.getSelectedItem(); 

        // 2. Validación de campos vacíos
        if (usuario.isEmpty() || clave.isEmpty()) {
            JOptionPane.showMessageDialog(this, 
                                          "Uno o ambos campos (Usuario/Contraseña) están vacíos. Por favor, ingrésalos.", 
                                          "Error de Validación", 
                                          JOptionPane.ERROR_MESSAGE);
        }else if (!usuario.matches("\\d+")) { 
                JOptionPane.showMessageDialog(this,
                        "El campo de usuario solo debe contener números (Documento de Identidad).",
                        "Error de Formato de Usuario",
                        JOptionPane.ERROR_MESSAGE);
        } else {
            // 3. Llamada a la lógica de autenticación (ControladorLogin)
            // USAMOS LA VARIABLE DE CLASE 'control' previamente declarada
            boolean loginExitoso = control.iniciarSesion(tipoUsuario, usuario, clave);

            if (loginExitoso) {
                // Credenciales correctas
                JOptionPane.showMessageDialog(this, 
                                              "¡Inicio de sesión exitoso como " + tipoUsuario + "!", 
                                              "Bienvenido", 
                                              JOptionPane.INFORMATION_MESSAGE);
                this.dispose();
                switch (tipoUsuario) {
                case "Funcionario ICA":
                    // Asume que tienes una clase llamada MenuFuncionario
                    MenuFuncionario menuF = new MenuFuncionario(usuario);
                    menuF.setVisible(true);
                    menuF.setLocationRelativeTo(null);
                    
                    break;
                case "Productor":
                    // Asume que tienes una clase llamada MenuProductor
                    MenuProductor menuProd = new MenuProductor(usuario);
                    menuProd.setVisible(true);
                    menuProd.setLocationRelativeTo(null);
                    break;
                case "Técnico":
                    // Asume que tienes una clase llamada MenuTecnico
                    MenuTecnico menuT = new MenuTecnico(usuario);
                    menuT.setVisible(true);
                    menuT.setLocationRelativeTo(null);
                    break;
                case "Propietario":
                    // Asume que tienes una clase llamada MenuPropietario
                    MenuPropietario menuProp = new MenuPropietario(usuario);
                    menuProp.setVisible(true);
                    menuProp.setLocationRelativeTo(null);
                    break;
                }
                // **[AQUÍ VA EL CÓDIGO PARA CONTINUAR]**
                // Por ejemplo: MenuPrincipal menu = new MenuPrincipal(); menu.setVisible(true); this.dispose();

            } else {
                // Credenciales incorrectas (falló en la BD)
                JOptionPane.showMessageDialog(this, 
                                              "Credenciales incorrectas o el usuario no existe como '" + tipoUsuario + "'.", 
                                              "Error de Autenticación", 
                                              JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_Btn_InicioSesActionPerformed

