    private void cargarDatosLugar(String documento, String numica) {
        
        Object[] datos = control.traerDatosLugar(documento, numica);
        
        if (datos != null) {
            // Índices: 0:documento, 1:usuario, 2:clave, 3:nombres, 4:apellidos, 5:telefono, 6:correo, 7:tarjetaProfesional
            
            Txt_num_predial.setText((String) datos[1]);
            Txt_nombre.setText((String) datos[2]);
            Txt_dep.setText((String) datos[3]); 
            Txt_mun.setText((String) datos[4]);
            Txt_vere.setText((String) datos[5]);
            Txt_num_lug_aso.setText((String) datos[6]);
            Txt_exte.setText((String) datos[7]);
            
        } else {
            JOptionPane.showMessageDialog(this, "No se encontró predio o hubo un error de conexión.", "Error de Busqueda", JOptionPane.ERROR_MESSAGE);
        }
    }

