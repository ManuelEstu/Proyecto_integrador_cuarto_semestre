    private void Btn_actualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Btn_actualizarActionPerformed
        String numica = Txt_numICA.getText().trim();
        String numpre = Txt_num_predial.getText().trim();
        String nombre = Txt_nombre.getText().trim();
        String dep = Txt_dep.getText().trim();
        String mun = Txt_mun.getText().trim();
        String vere = Txt_vere.getText().trim();
        String lug = Txt_num_lug_aso.getText().trim();
        String ext = Txt_exte.getText().trim();
        String documento = Txt_doc_dueno.getText().trim();

        String error = null;
        if (error == null) error = control.validarNombre(nombre);
        if (error == null) error = control.validarNumeros(lug, ext);
        if (error != null) {
            // Muestra el primer error encontrado
            JOptionPane.showMessageDialog(this, error, "Error de Validación", JOptionPane.WARNING_MESSAGE);
            return;
        }

        DatosPredio datosActualizados = new DatosPredio(
            numica,                       // 1. documento (desde el campo de texto, aunque se podría usar this.documento)
            numpre,         // 2. tipoUsuario (¡ESTA ES LA CLAVE! Debe ser "Funcionario ICA", etc.)
            nombre,                   // 3. nombreUsuario
            dep,                     // 4. clave
            mun,                    // 5. nombre
            vere,                  // 6. apellido
            lug,                  // 7. telefono
            ext,                    // 8. correo
            documento                     // 9. tarjetaProfesional
        );

        // 4. Intentar la actualización
        boolean exito = control.actualizarDatos(datosActualizados);

        if (exito) {
            JOptionPane.showMessageDialog(this, "✅ Lugar de producción actualizado exitosamente.", "Éxito", JOptionPane.INFORMATION_MESSAGE);

            // 5. Regresar al menú correspondiente (Mantener el flujo de navegación)
            this.dispose();
            MenuPropietario menuPropie = new MenuPropietario(this.documento);
            menuPropie.setVisible(true);
            menuPropie.setLocationRelativeTo(null);
        }else {
            JOptionPane.showMessageDialog(this, "❌ Error al actualizar el predio. Revise la conexión y los datos.", "Error de Actualización", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_Btn_actualizarActionPerformed

