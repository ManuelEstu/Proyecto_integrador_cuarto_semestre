    private void Btn_registrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Btn_registrarActionPerformed
        String tipoUsuario = (String) Seleccion_user.getSelectedItem();
    boolean esTecnico = "Técnico".equals(tipoUsuario);

    // 1. Validar que todos los campos obligatorios estén llenos
    if (!validarCamposVacios(esTecnico)) {
        return;
    }

    // 2. Validar que la Contraseña sea igual a Confirmar Contraseña
    String clave = new String(Txt_clave.getPassword());
    String confirmarClave = new String(Txt_confir_clave.getPassword());

    if (!clave.equals(confirmarClave)) {
        JOptionPane.showMessageDialog(this, "Las contraseñas no coinciden.", "Error de Validación", JOptionPane.ERROR_MESSAGE);
        return;
    }

    // --- Preparación de datos ---
    String documentoStr = Txt_documento.getText().trim();
    String telefonoStr = Txt_telefono.getText().trim();
    String tarjetaProfesional = Txt_tar_pro.getText().trim(); // Solo para Técnicos

    // Validación de formato numérico básica
    if (!documentoStr.matches("\\d+") || !telefonoStr.matches("\\d+")) {
        JOptionPane.showMessageDialog(this, "El Documento y el Teléfono deben contener solo números.", "Error de Formato", JOptionPane.ERROR_MESSAGE);
        return;
    }

    // Validación específica de formato para la Tarjeta Profesional si es Técnico
    if (esTecnico && !tarjetaProfesional.matches("\\d+")) {
         // Esta validación se hará más robusta en el controlador, pero previene entradas obvias.
         JOptionPane.showMessageDialog(this, "La Tarjeta Profesional debe contener solo números.", "Error de Formato", JOptionPane.ERROR_MESSAGE);
         return;
    }

    long documento;
    long telefono;
    try {
        documento = Long.parseLong(documentoStr);
        telefono = Long.parseLong(telefonoStr);
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "Error interno al procesar números. Verifique Documento y Teléfono.", "Error de Conversión", JOptionPane.ERROR_MESSAGE);
        return;
    }

    // Obtener los datos restantes
    String nomUser = Txt_nom_user.getText().trim();
    String nombre = Txt_nombre.getText().trim();
    String apellido = Txt_apellido.getText().trim();
    String correo = Txt_correo.getText().trim();

    String mensajeError = null;

    // 3. Condición principal: Validar y Registrar con el controlador adecuado
    if (esTecnico) {
        // Opción 1: Es Técnico (usa ControladorTecnico, que incluye Tarjeta Profesional)
        mensajeError = controlTecnico.validarRegistroTecnico(
            documento, nomUser, clave, nombre, apellido, telefono, correo, tarjetaProfesional);
    } else {
        // Opción 2: Es Funcionario ICA, Productor o Propietario (usa ControladorUser1)
        // ControladorUser1 debe ahora manejar el guardado de Funcionario ICA.
        mensajeError = control.validarRegistro(
            tipoUsuario, documento, nomUser, clave, nombre, apellido, telefono, correo);
    }

    // 4. Manejo del resultado del proceso de registro
    if (mensajeError == null) {
        // Registro exitoso (validación OK y guardado en BD OK)
        JOptionPane.showMessageDialog(this,
                    "¡Registro exitoso! Los datos han sido guardados para el usuario " + tipoUsuario + ".",
                    "Registro Exitoso", JOptionPane.INFORMATION_MESSAGE);
        limpiarCampos();
    } else {
        // Registro fallido (falló alguna validación o el proceso de guardado en BD)
        JOptionPane.showMessageDialog(this,
                    "Error en el registro: " + mensajeError,
                    "Error de Registro", JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_Btn_registrarActionPerformed
    
    private void limpiarCampos() {
        Txt_documento.setText("");
        Txt_nom_user.setText("");
        Txt_clave.setText(""); // Limpia JPasswordField
        Txt_confir_clave.setText(""); // Limpia JPasswordField
        Txt_nombre.setText("");
        Txt_apellido.setText("");
        Txt_telefono.setText("");
        Txt_correo.setText("");
        Txt_tar_pro.setText("");
        // Opcionalmente, puedes restablecer el ComboBox al primer elemento
        // Seleccion_user.setSelectedIndex(0); 
    }
    
    private boolean validarCamposVacios(boolean esTecnico) {
        // Campos de texto comunes
        if (Txt_documento.getText().trim().isEmpty() ||
            Txt_nom_user.getText().trim().isEmpty() ||
            String.valueOf(Txt_clave.getPassword()).trim().isEmpty() ||
            String.valueOf(Txt_confir_clave.getPassword()).trim().isEmpty() ||
            Txt_nombre.getText().trim().isEmpty() ||
            Txt_apellido.getText().trim().isEmpty() ||
            Txt_telefono.getText().trim().isEmpty() ||
            Txt_correo.getText().trim().isEmpty()) {
            
            JOptionPane.showMessageDialog(this, "Debe llenar todos los campos obligatorios.", "Error de Validación", JOptionPane.ERROR_MESSAGE);
            return false;
        }

        // Validación específica para Técnico
        if (esTecnico && Txt_tar_pro.getText().trim().isEmpty()) {
             JOptionPane.showMessageDialog(this, "Para el tipo de usuario Técnico, la Tarjeta Profesional es obligatoria.", "Error de Validación", JOptionPane.ERROR_MESSAGE);
            return false;
        }

        return true;
    }
    
