    private void Btn_generarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Btn_generarActionPerformed
       // 1. Obtener el texto del campo (Nro ICA)
        String icaNumber = Txt_numica.getText();

        try {
            // 2. Llamar al controlador para obtener el informe
            Optional<InformeEstadoLug> informeOpt = controller.generarInforme(icaNumber);

            // 3. Procesar el resultado
            if (informeOpt.isPresent()) {
                
                InformeEstadoLug informe = informeOpt.get();
                
                // --- ¡CAMBIO CRUCIAL AQUÍ! ---
                // Cierra esta ventana y abre la vista de resultados, pasando el objeto informe
                this.dispose(); 
                InformeEstLug resultadosView = new InformeEstLug(informe, this.documento);
                resultadosView.setVisible(true);
                resultadosView.setLocationRelativeTo(null);
                // -----------------------------

            } else {
                // No se encontró ningún Lugar de Producción con ese ICA
                JOptionPane.showMessageDialog(this,
                    "⚠️ No se encontró un Lugar de Producción con el ICA: " + icaNumber,
                    "Resultado de Búsqueda",
                    JOptionPane.WARNING_MESSAGE);
            }

        } catch (IllegalArgumentException e) {
            // Error de validación (ICA no tiene 6 dígitos o está vacío)
            JOptionPane.showMessageDialog(this,
                "❌ Error de Validación: " + e.getMessage(),
                "Error de Entrada",
                JOptionPane.ERROR_MESSAGE);
        } catch (RuntimeException e) {
            // Error de Base de Datos (capturado del controlador)
             JOptionPane.showMessageDialog(this,
                "❌ Error del Sistema: Ocurrió un problema al acceder a la base de datos.",
                "Error Crítico",
                JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_Btn_generarActionPerformed

